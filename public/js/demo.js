// Reload cart
function reloadCart() {
    $.ajaxSetup({
        headers: {
            'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
        }
    });
    $.ajax({
        url: CHART_URL,
        datType: 'json',
        success: function (response) {
            html = '';
            jumlahBarang = 0;
            $.each(response, function (index, item) {
                jumlahBarang += parseInt(item['qty'])
                html += '<li>'
                    + '<a href="javascript:void(0);" class=" waves-effect waves-block">'
                    + '<div class="icon-circle bg-cyan">'
                    + '<i class="material-icons">add_shopping_cart</i>'
                    + '</div>'
                    + '<div class="menu-info">'
                    + '<h4>' + item['nama'] + '</h4>'
                    + '<p>'
                    + '<span class="badge badge-success">' + item['qty'] + 'x</span> Rp. ' + addCommas(item['hargajual']) + '</span>'
                    + '</p>'
                    + '</div>'
                    + '</a>'
                    + '</li>'
            });


            if (jumlahBarang > 0) {
                $('.dropdown-content-footer').removeClass('hide');
            } else {
                html = '<span class="text-danger">Tidak Ada Item di Keranjang Belanja</span>';
            }

            $('#keranjangBelanjaDetail').html(html)

            $('.jumlah-transaksi').html(jumlahBarang)

        }
    })

}

function addCommas(nStr) {
    nStr += '';
    var x = nStr.split(',');
    var x1 = x[0];
    var x2 = x.length > 1 ? ',' + x[1] : '';
    var rgx = /(\d+)(\d{3})/;
    while (rgx.test(x1)) {
        x1 = x1.replace(rgx, '$1' + '.' + '$2');
    }
    return x1 + x2;
}

//Skin changer
function skinChanger() {
    $('.right-sidebar .demo-choose-skin li').on('click', function () {
        var $body = $('body');
        var $this = $(this);

        var existTheme = $('.right-sidebar .demo-choose-skin li.active').data('theme');
        $('.right-sidebar .demo-choose-skin li').removeClass('active');
        $body.removeClass('theme-' + existTheme);
        $this.addClass('active');

        $body.addClass('theme-' + $this.data('theme'));
    });
}

//Skin tab content set height and show scroll
function setSkinListHeightAndScroll(isFirstTime) {
    var height = $(window).height() - ($('.navbar').innerHeight() + $('.right-sidebar .nav-tabs').outerHeight());
    var $el = $('.demo-choose-skin');

    if (!isFirstTime) {
        $el.slimScroll({destroy: true}).height('auto');
        $el.parent().find('.slimScrollBar, .slimScrollRail').remove();
    }

    $el.slimscroll({
        height: height + 'px',
        color: 'rgba(0,0,0,0.5)',
        size: '6px',
        alwaysVisible: false,
        borderRadius: '0',
        railBorderRadius: '0'
    });
}

